/// Stable identifiers for melee weapons.
///
/// These IDs are intended to be deterministic and allocation-light, similar to
/// projectile item IDs used for the projectile slot.
enum WeaponId {
  /// Baseline one-handed sword.
  plainsteel,
  waspfang,
  cinderedge,
  basiliskKiss,
  frostbrand,
  stormneedle,
  nullblade,
  sunlitVow,
  graveglass,
  duelistsOath,
  woodenShield,
  basicShield,
  solidShield,
}
